struct AboutView: View {
    @State var versionString: String = "Version \(appVersion)"
    let versionName: String = "Forever in development"
    var body: some View {
            VStack {
                Image("AppIcon-preview")
                    .resizable()
                    .frame(width: 100, height: 100)
                    .padding(.top, 50)
                    .cornerRadius(24)
                Text(Bundle.main.infoDictionary!["CFBundleName"] as? String ?? "OpenPicasso")
                    .font(.title.weight(.bold))
                Text("\(versionString)\n")
                    .foregroundColor(.secondary)
                    .transition(.opacity)
                    .multilineTextAlignment(.center)
                List {
                    Link(destination: .init(string: "https://github.com/sourcelocation/Picasso-v3/tree/opensource")!, label: {
                        Label("OpenPicasso Source Code", systemImage: "curlybraces")
                    })
                    NavigationLink(destination: LicencesView(), label: {
                        Label("Licence Notices", systemImage: "text.book.closed")
                    })
                }
                .navigationTitle("About OpenPicasso")
                .padding()
            }
            .onAppear {
                DispatchQueue.main.asyncAfter(deadline: .now() + 3.0) {
                    //                versionString += "\nThe “\(versionName)” release"
                }
            }
            .background(Color(UIColor.systemGroupedBackground))
        }
}

